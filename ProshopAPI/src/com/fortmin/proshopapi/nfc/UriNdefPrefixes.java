package com.fortmin.proshopapi.nfc;

public class UriNdefPrefixes {
	
	public static final byte NONE = 0x00;			// Nada
	public static final byte HTTP_WWW = 0x01;		// http://www.
	public static final byte HTTPS_WWW = 0x02;		// https://www.
	public static final byte HTTP = 0x03;			// http://
	public static final byte HTTPS = 0x04;			// https://
	public static final byte TEL = 0x05;			// tel:
	public static final byte MAILTO = 0x06;			// mailto:
	public static final byte FTPANONYMOUS = 0x07;	// ftp://anonymous:anonymous@
	public static final byte FTP = 0x08;			// ftp://ftp.
	public static final byte FTPS = 0x09;			// ftps://
	public static final byte SFTP = 0x0A;			// sftp://
	public static final byte SMB = 0x0B;			// smb://
	
}
